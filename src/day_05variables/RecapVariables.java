package day_05variables;

public class RecapVariables {

    public static void main(String[] args) {
  //how to declare:
        byte numberOfCoffeCups;
        double totalPriceOfCoffe;
        int totalStudentInClass;


        // How to assign:
        numberOfCoffeCups = 2;
        totalPriceOfCoffe = 10.25;
        totalStudentInClass = 432;


        System.out.println(numberOfCoffeCups);
        System.out.println(totalPriceOfCoffe);
        System.out.println(totalStudentInClass);

        System.out.println(numberOfCoffeCups + " * " + totalStudentInClass);

        //Declare and Assign

        double temperature = 20.6;
        int javaDays = 5;

        System.out.println("\nThis is my temperature: " + temperature);
        System.out.println("It is day" + javaDays + " in java class");







    }







}
